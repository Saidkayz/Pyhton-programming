# Arithmetic operators
x = 10
y = 4
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) # Modulus - returns the remainder after dividing two number



# Comparison operators
a = 9
b = 78
print(a < b)
print(a > b)
print(a >= b)
print(a >= b)
print(a == b)  # equal to
print(a != b)  # not equal to

# Assignment Operators
w = 7
print(w)

y = 12
